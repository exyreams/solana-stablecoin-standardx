	// ── Factory ────────────────────────────────────────────────────────────────

	/**
	 * Create a new stablecoin from a preset or custom config.
	 *
	 * @example
	 * // SSS-1 preset
	 * const stable = await SolanaStablecoin.create(connection, {
	 *   preset: Presets.SSS_1,
	 *   name: 'My Dollar',
	 *   symbol: 'MYDOL',
	 *   decimals: 6,
	 *   authority: adminKeypair,
	 * });
	 *
	 * // SSS-2 preset
	 * const compliant = await SolanaStablecoin.create(connection, {
	 *   preset: Presets.SSS_2,
	 *   name: 'Compliant USD',
	 *   symbol: 'CUSD',
	 *   authority: adminKeypair,
	 *   transferHookProgramId: hookProgramId,
	 * });
	 *
	 * // Custom config (no preset)
	 * const custom = await SolanaStablecoin.create(connection, {
	 *   name: 'Custom',
	 *   symbol: 'CUST',
	 *   enablePermanentDelegate: true,
	 *   enableConfidentialTransfers: true,
	 *   authority: adminKeypair,
	 * });
	 */
	static async create(
		connection: Connection,
		options: CreateOptions & {
			authority: Keypair;
			mintKeypair?: Keypair;
			programId?: PublicKey;
			oracleProgramId?: PublicKey;
		},
	): Promise<{ stablecoin: SolanaStablecoin; signature: string }> {
		const wallet = new Wallet(options.authority);
		const provider: AnchorProvider = new AnchorProvider(connection, wallet, {
			commitment: "confirmed",
		});
		const program = new Program(IDL as any, provider);

		const preset = options.preset ?? SSS_1;
		const ext = options.extensions ?? {};

		const mintKeypair = options.mintKeypair ?? Keypair.generate();

		// Resolve config: explicit options > extensions > preset
		const enableMintCloseAuthority = options.enableMintCloseAuthority ?? false; // Default false for Metaplex compatibility
		const enablePermanentDelegate =
			options.enablePermanentDelegate ??
			ext.permanentDelegate ??
			preset.enablePermanentDelegate;
		const enableTransferHook =
			options.enableTransferHook ??
			ext.transferHook ??
			preset.enableTransferHook;
		const defaultAccountFrozen =
			options.defaultAccountFrozen ??
			ext.defaultAccountFrozen ??
			preset.defaultAccountFrozen;
		const enableConfidentialTransfers =
			options.enableConfidentialTransfers ?? preset.enableConfidentialTransfers;
		const confidentialTransferAutoApprove =
			options.confidentialTransferAutoApprove ??
			preset.confidentialTransferAutoApprove;

		// Convert auditor pubkey to number array for Anchor serialization
		const auditorElgamalPubkey = options.auditorElGamalPubkey
			? Array.from(options.auditorElGamalPubkey)
			: null;

		const config = {
			name: options.name,
			symbol: options.symbol,
			uri: options.uri ?? "",
			decimals: options.decimals ?? 6,
			enableMintCloseAuthority,
			enablePermanentDelegate,
			enableTransferHook,
			defaultAccountFrozen,
			transferHookProgramId: options.transferHookProgramId ?? null,
			enableConfidentialTransfers,
			confidentialTransferAutoApprove,
			auditorElgamalPubkey: auditorElgamalPubkey,
		};

		const signature = await (program.methods as any)
			.initialize(config)
			.accounts({
				authority: options.authority.publicKey,
				mint: mintKeypair.publicKey,
				tokenProgram: TOKEN_2022_PROGRAM_ID,
			})
			.signers([mintKeypair])
			.rpc();

		const hookProgramId = enableTransferHook
			? (options.transferHookProgramId ?? DEFAULT_TRANSFER_HOOK_PROGRAM_ID)
			: undefined;

		return {
			stablecoin: new SolanaStablecoin(
				program,
				connection,
				options.authority,
				mintKeypair.publicKey,
				hookProgramId,
				options.oracleProgramId,
			),
			signature,
		};
	}

	/**
	 * Load an existing stablecoin by mint address.
	 *
	 * If the mint has a transfer hook enabled, the SDK will default to the
	 * project's transfer hook program ID. Pass `options.transferHookProgramId`
	 * to override.
	 */
	static async load(
		connection: Connection,
		mint: PublicKey,
		authority: Keypair,
		options?: {
			programId?: PublicKey;
			transferHookProgramId?: PublicKey;
			oracleProgramId?: PublicKey;
		},
	): Promise<SolanaStablecoin> {
		const id =
			options?.programId ?? new PublicKey(IDL.address ?? IDL.metadata?.address);
		const wallet = new Wallet(authority);
		const provider: AnchorProvider = new AnchorProvider(connection, wallet, {
			commitment: "confirmed",
		});
		const program = new Program(IDL as any, provider);

		// Auto-detect transfer hook: read state and default to project hook program
		let hookProgramId = options?.transferHookProgramId;
		if (!hookProgramId) {
			try {
				const [statePda] = deriveStablecoinState(mint, id);
				const state = await (program.account as any).stablecoinState.fetch(
					statePda,
				);
				if (state.enableTransferHook) {
					hookProgramId = DEFAULT_TRANSFER_HOOK_PROGRAM_ID;
				}
			} catch {
				// State not readable — skip hook auto-detection
			}
		}

		return new SolanaStablecoin(
			program,
			connection,
			authority,
			mint,
			hookProgramId,
			options?.oracleProgramId,
		);
	}

