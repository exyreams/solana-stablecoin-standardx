	/**
	 * Update role assignments. Only the master authority can call this.
	 * Only provided fields are updated; omitted fields keep their current value.
	 */
	async updateRoles(updates: RolesUpdate): Promise<string> {
		const [statePda] = deriveStablecoinState(this.mint, this.program.programId);
		const [rolesPda] = deriveRolesConfig(this.mint, this.program.programId);

		return (this.program.methods as any)
			.updateRoles({
				burner: updates.burner ?? null,
				pauser: updates.pauser ?? null,
				blacklister: updates.blacklister ?? null,
				seizer: updates.seizer ?? null,
			})
			.accounts({
				authority: this.authority.publicKey,
				stablecoinState: statePda,
				rolesConfig: rolesPda,
			})
			.signers([this.authority])
			.rpc();
	}

	/**
	 * Initiate master authority transfer (step 1 of 2).
	 * The current master calls this to propose a new master.
	 * The new master must call `acceptAuthority()` to complete the transfer.
	 */
	async transferAuthority(newMaster: PublicKey): Promise<string> {
		const [statePda] = deriveStablecoinState(this.mint, this.program.programId);
		const [rolesPda] = deriveRolesConfig(this.mint, this.program.programId);

		return (this.program.methods as any)
			.transferAuthority(newMaster)
			.accounts({
				caller: this.authority.publicKey,
				stablecoinState: statePda,
				rolesConfig: rolesPda,
			})
			.signers([this.authority])
			.rpc();
	}

	/**
	 * Accept pending master authority transfer (step 2 of 2).
	 * The pending master calls this to finalize the transfer.
	 */
	async acceptAuthority(): Promise<string> {
		const [statePda] = deriveStablecoinState(this.mint, this.program.programId);
		const [rolesPda] = deriveRolesConfig(this.mint, this.program.programId);

		return (this.program.methods as any)
			.transferAuthority(null)
			.accounts({
				caller: this.authority.publicKey,
				stablecoinState: statePda,
				rolesConfig: rolesPda,
			})
			.signers([this.authority])
			.rpc();
	}

